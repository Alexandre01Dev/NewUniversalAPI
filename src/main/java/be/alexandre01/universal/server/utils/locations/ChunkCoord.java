package be.alexandre01.universal.server.utils.locations;

public class ChunkCoord {
    private int x;
    private int z;

    public ChunkCoord(int x, int z) {
        this.x = x;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getZ() {
        return z;
    }

}
